     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $get=Warehouse::where('id',$id)->first();
        $locationinfo=WarehouseLocation::where('id',$get->location)->first();
        return response()->json($locationinfo);
  
    }

